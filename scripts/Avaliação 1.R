Avls <- fitsad(EstAv$Soma, "ls")
summary(Avls)
coef(Avls)
logLik(Avls)
AIC(Avls)
AvPerfilGram<-profile(Avls)
likelregions(AvPerfilGram)
confint(AvPerfilGram)
plotprofmle(AvPerfilGram)
plot(AvPerfilGram)  
